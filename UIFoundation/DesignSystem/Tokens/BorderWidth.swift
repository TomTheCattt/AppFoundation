import UIKit

struct DesignSystemBorderWidth {
    /// No border
    static let none: CGFloat = 0

    /// 1pt - Default borders
    static let thin: CGFloat = 1

    /// 2pt - Emphasis
    static let medium: CGFloat = 2

    /// 4pt - Strong emphasis
    static let thick: CGFloat = 4
}
